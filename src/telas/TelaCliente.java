    private void txtfRgKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfRgKeyTyped
        String nameInput = this.txtfNome.getText();
        String telefoneInput = this.txtfTelefone.getText();
        String rgInput = this.txtfRg.getText();
        String cpfInput = this.txtfCpf.getText();
        
        if(nameInput.length() > 0 && telefoneInput.length() > 0 && rgInput.length() > 0 && cpfInput.length() > 0 && !this.btnAlterar.isEnabled()) {
            this.btnSalvar.setEnabled(true);
        } else {
            this.btnSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_txtfRgKeyTyped

