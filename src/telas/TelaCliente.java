    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        userId++;
        String nome = this.txtfNome.getText();
        String Cpf = this.txtfCpf.getText();
        String Rg = this.txtfRg.getText();
        String telefone = this.txtfTelefone.getText();
        
        Cliente cliente = new Cliente(userId, Rg, Cpf, nome, telefone);
        
        clientes.add(cliente);
        
        UpdateTable();
        
        this.txtfNome.setText("");
        this.txtfCpf.setText("");
        this.txtfRg.setText("");
        this.txtfTelefone.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

