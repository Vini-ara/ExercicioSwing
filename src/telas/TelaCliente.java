    private void jtblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblTableMouseClicked
        int index = this.jtblTable.getSelectedRow();
        
        if(index < 0 || index >= clientes.size()) return;
        
        Cliente cliente = clientes.get(index);
        
        this.txtfCpf.setText(cliente.getCPF());
        this.txtfRg.setText(cliente.getRG());
        this.txtfNome.setText(cliente.getNome());
        this.txtfTelefone.setText(cliente.getTelefone());
        
        this.idUserSelecionado = index + 1;
        
        this.btnSalvar.setEnabled(false);
        this.btnAlterar.setEnabled(true);
        this.btnExcluir.setEnabled(true);
    }//GEN-LAST:event_jtblTableMouseClicked

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String nameInput = this.txtfNome.getText();
        String telefoneInput = this.txtfTelefone.getText();
        String rgInput = this.txtfRg.getText();
        String cpfInput = this.txtfCpf.getText();
        
        Cliente cliente = clientes.get(this.idUserSelecionado - 1);
        
        cliente.setCPF(cpfInput);
        cliente.setNome(nameInput);
        cliente.setRG(rgInput);
        cliente.setTelefone(telefoneInput);
        
        this.UpdateTable();
        
        
        this.txtfNome.setText("");
        this.txtfCpf.setText("");
        this.txtfRg.setText("");
        this.txtfTelefone.setText("");
        
        this.btnAlterar.setEnabled(false);
        this.btnCancelar.setEnabled(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        clientes.remove(this.idUserSelecionado - 1);
        
        this.txtfNome.setText("");
        this.txtfCpf.setText("");
        this.txtfRg.setText("");
        this.txtfTelefone.setText("");
        
        UpdateTable();
        
        this.btnAlterar.setEnabled(false);
        this.btnExcluir.setEnabled(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

