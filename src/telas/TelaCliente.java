    private void UpdateTable() {
        DefaultTableModel model = (DefaultTableModel) this.jtblTable.getModel();
        
        for(int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }
        
        for(int i = 0; i < clientes.size(); i++) {
            Object[] row = new Object[5];
            
            row[0] = clientes.get(i).getId();
            row[1] = clientes.get(i).getRG();
            row[2] = clientes.get(i).getCPF();
            row[3] = clientes.get(i).getNome();
            row[4] = clientes.get(i).getTelefone();
            
            model.addRow(row);
        }
        
        this.jtblTable.setModel(model);
    }

